const num = 5;
const anotherNum = 8;

console.log(num == anotherNum); //false
console.log(num > anotherNum); //false
console.log(num < anotherNum); //true
console.log(num >= anotherNum); //false
console.log(num <= anotherNum); //true

console.log(num == "5"); //true
console.log(num != 8); //true
console.log(num != "5"); //false